#include "3-calc.h"

/**
 * op_add - addition operation
 * @b: second integer
 * @a: first integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction operation
 * @b: second integer
 * @a: first integer
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication operation
 * @b: second integer
 * @a: first integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division operation
 * @b: second integer
 * @a: first integer
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus division operation
 * @b: second integer
 * @a: first integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
